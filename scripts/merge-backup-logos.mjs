#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';

const GENERATED = path.join(process.cwd(), 'src/app/data/teams.maxpreps.generated.ts');
const BACKUP = path.join(process.cwd(), '.maxpreps-backups', 'backup-2026-04-22T05-06-47', 'teams.ts');

async function parseGenerated(file) {
  const txt = await fs.readFile(file, 'utf8');
  const m = txt.match(/export const maxprepsTeamData[\s\S]*?=\s*(\{[\s\S]*?\});/);
  if (!m) throw new Error('Could not locate maxprepsTeamData object');
  const jsonText = m[1];
  // JSON.parse should work because file was generated with JSON.stringify
  const obj = JSON.parse(jsonText);
  return obj;
}

async function parseBackupImages(file) {
  const txt = await fs.readFile(file, 'utf8');
  const out = new Map();
  const blockRegex = /id:\s*"([^\"]+)"([\s\S]*?)image:\s*(?:GENERIC_LOGO|"([^\"]+)")/g;
  let m;
  while ((m = blockRegex.exec(txt)) !== null) {
    const id = m[1];
    const img = m[3] || null;
    if (img) out.set(id, img);
  }
  return out;
}

async function main() {
  const generatedExists = await fs.access(GENERATED).then(()=>true).catch(()=>false);
  if (!generatedExists) {
    console.error('Generated file not found:', GENERATED);
    process.exit(1);
  }

  const backupExists = await fs.access(BACKUP).then(()=>true).catch(()=>false);
  if (!backupExists) {
    console.error('Backup teams.ts not found:', BACKUP);
    process.exit(1);
  }

  const gen = await parseGenerated(GENERATED);
  const backupImgs = await parseBackupImages(BACKUP);

  let replaced = 0;
  for (const [id, data] of Object.entries(gen)) {
    const need = !data.schoolMascotUrl;
    if (need) {
      const b = backupImgs.get(id);
      if (b && typeof b === 'string' && b.startsWith('http')) {
        data.schoolMascotUrl = b;
        replaced += 1;
      }
    }
  }

  if (replaced === 0) {
    console.log('No missing mascots found in backup to merge');
    return;
  }

  const generatedAt = new Date().toISOString();
  const header = `// This file is auto-generated by scripts/update-maxpreps-teams.mjs\n// Merged backup mascots by scripts/merge-backup-logos.mjs\n// Generated at: ${generatedAt}\n\n`;
  const body = `export const MAXPREPS_TEAMS_GENERATED_AT = ${JSON.stringify(generatedAt)};\n\nexport type MaxprepsTeamData = {\n  maxprepsUrl?: string;\n  leagueName?: string;\n  schoolMascotUrl?: string;\n  stateRank?: number;\n  record?: { wins: number; losses: number };\n  pointsFor?: number;\n  pointsAgainst?: number;\n  streak?: string;\n  lastUpdated?: string;\n};\n\nexport const maxprepsTeamData: Record<string, MaxprepsTeamData> = ${JSON.stringify(gen, null, 2)};\n`;
  await fs.writeFile(GENERATED, header + body, 'utf8');
  console.log(`Merged ${replaced} mascots into ${GENERATED}`);
}

main().catch(e=>{console.error(e); process.exit(1)});
