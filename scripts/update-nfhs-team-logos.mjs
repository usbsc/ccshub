#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";

const TEAMS_PATH = path.join(process.cwd(), "src/app/data/teams.ts");
const OUT_PATH = path.join(process.cwd(), "src/app/data/teams.nfhs.generated.ts");

function normalizeText(s) {
  return (s || "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function tokens(s) {
  const t = normalizeText(s);
  return t ? t.split(/\s+/).filter(Boolean) : [];
}

function overlap(a, b) {
  const ta = new Set(tokens(a));
  const tb = new Set(tokens(b));
  if (!ta.size || !tb.size) return 0;
  let n = 0;
  for (const t of ta) if (tb.has(t)) n += 1;
  return n / Math.max(ta.size, tb.size);
}

function parseTeams(tsText) {
  const out = [];
  const re = /\{\s*id:\s*"([^"]+)"[\s\S]*?name:\s*"([^"]+)"[\s\S]*?mascot:\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(tsText))) out.push({ id: m[1], name: m[2], mascot: m[3] });
  return out;
}

function schoolSearchUrl(team) {
  const q = [team.name, team.mascot && team.mascot !== "N/A" ? team.mascot : ""].filter(Boolean).join(" ");
  return `https://search-api.nfhsnetwork.com/v3/search/schools?search_term=${encodeURIComponent(q)}&size=10`;
}

async function fetchSchoolLogo(team) {
  const res = await fetch(schoolSearchUrl(team), {
    headers: { "user-agent": "ccshub-nfhs-logo-sync/1.0", accept: "application/json" },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} ${res.statusText}`);
  const json = await res.json();
  const items = Array.isArray(json.items) ? json.items : [];
  let best = null;
  let bestScore = -1;
  for (const s of items) {
    if ((s.state_code || "").toUpperCase() !== "CA") continue;
    const score = overlap(team.name, s.name) * 10 + overlap(team.mascot, s.mascot) * 6;
    if (score > bestScore && typeof s.logo === "string" && s.logo.startsWith("http")) {
      best = s;
      bestScore = score;
    }
  }
  return best?.logo || null;
}

async function main() {
  const tsText = await fs.readFile(TEAMS_PATH, "utf8");
  const teams = parseTeams(tsText);
  const out = {};
  for (const team of teams) {
    try {
      const logo = await fetchSchoolLogo(team);
      if (logo) out[team.id] = logo;
      console.log(`${team.id}: ${logo ? "ok" : "missing"}`);
    } catch (e) {
      console.log(`${team.id}: error`);
    }
  }

  const file =
    `// Auto-generated by scripts/update-nfhs-team-logos.mjs\n` +
    `export const nfhsTeamLogos: Record<string, string> = ${JSON.stringify(out, null, 2)};\n`;
  await fs.writeFile(OUT_PATH, file, "utf8");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
