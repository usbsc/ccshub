function normalizePlayerName(name: string) {
  return (name || "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function playerKey(p: Pick<Player, "team" | "name">) {
  return `${p.team}:${normalizePlayerName(p.name)}`;
}

export const players: Player[] = (() => {
  const teamsWithMaxpreps = new Set(maxprepsPlayers.map((p) => p.team));
  const manualByKey = new Map(manualPlayers.map((p) => [playerKey(p), p]));

  const mergedMaxpreps = maxprepsPlayers.map((mp) => {
    const manual = manualByKey.get(playerKey(mp));

    return {
      ...mp,
      number: mp.number || manual?.number || 0,
      grade: mp.grade || manual?.grade || 0,
      height: mp.height || manual?.height || "",
      weight: mp.weight || manual?.weight || 0,
      highlights: mp.highlights?.length ? mp.highlights : manual?.highlights,
    };
  });

  const manualForMissingTeams = manualPlayers.filter((p) => !teamsWithMaxpreps.has(p.team));

  return [...mergedMaxpreps, ...manualForMissingTeams].sort((a, b) =>
    (a.team + a.name).localeCompare(b.team + b.name)
  );
})();
